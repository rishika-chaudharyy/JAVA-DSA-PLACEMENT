//Write a function to print product of two numbers

public class function_product {
    public static int prod(int a,int b){
        int prod=a*b;
        return prod;
    }
    public static void main(String args[]){
        int a=4;
        int b=5;
    int product=prod(a,b);
    System.out.println(product);
    }
}
