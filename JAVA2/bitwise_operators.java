//Write a program for all the bitwise operators
public class bitwise_operators {
    public static void main(String args[]){
        System.out.println(5&6);
        System.out.println(5|6);
        System.out.println(5^6);
        System.out.println(~5);
        System.out.println(5>>2);
        System.out.println(5<<2);

    }
}
